package operators;

public class BitwiseOperator {

	public static void main(String[] args) {
		System.out.println(12&10);
		System.out.println(12|10);
		System.out.println(12^10);
		System.out.println(12^10^12);
		System.out.println(5<<3);
		System.out.println(-15>>3);
		System.out.println(15>>3  );
	}

}
                                                                                                                                                                           